package fabulousoft.rpgtools.objects;


public class Adjective implements Word {
	
	public String	baseForm;
	
	
	public Adjective(String adj) {
	
		baseForm = adj;
	}
	
	
	@Override
	public String toString() {
	
		return baseForm;
	}


	@Override
	public String wordType() {
	
		return "Adjective";
	}
}
